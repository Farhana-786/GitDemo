package naveenJavaExamp;

public class Sample3 {

	public static void main(String[] args) {
      
		//Loops in java(similar kind of code 
		//1.While loop  //infinite loop
		int i=1;//Initialization
		while(i<=10) {  //conditional
			System.out.println(i);
			i=i+1;  //incremental/decremental
		}
		System.out.println("********");
		//Print 1 to 10
		//for Loop
		for(int j=1;j<=10;j++) {  //Initialization,conditional,Incremental
			System.out.println(j);
		}
		System.out.println("********");
		//print 10 to 1
		for(int k=10;k>=-10;k--) { //Initialization,conditional,decremental
			System.out.println(k);
		}
	}

}
